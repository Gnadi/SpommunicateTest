public class Positionen {
    private String positionartId;
    private String name;
    private String kurzzeichen;

    public String getPositionartId() {
        return positionartId;
    }

    public void setPositionartId(String positionartId) {
        this.positionartId = positionartId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getKurzzeichen() {
        return kurzzeichen;
    }

    public void setKurzzeichen(String kurzzeichen) {
        this.kurzzeichen = kurzzeichen;
    }
}
